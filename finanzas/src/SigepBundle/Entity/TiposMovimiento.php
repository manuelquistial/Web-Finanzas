<?php

namespace SigepBundle\Entity;

/**
 * TiposMovimiento
 */
class TiposMovimiento
{
    /**
     * @var string
     */
    private $nombre;

    /**
     * @var boolean
     */
    private $tipoEgreso = '1';

    /**
     * @var integer
     */
    private $codigo;


    /**
     * Set nombre
     *
     * @param string $nombre
     *
     * @return TiposMovimiento
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get nombre
     *
     * @return string
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Set tipoEgreso
     *
     * @param boolean $tipoEgreso
     *
     * @return TiposMovimiento
     */
    public function setTipoEgreso($tipoEgreso)
    {
        $this->tipoEgreso = $tipoEgreso;

        return $this;
    }

    /**
     * Get tipoEgreso
     *
     * @return boolean
     */
    public function getTipoEgreso()
    {
        return $this->tipoEgreso;
    }

    /**
     * Get codigo
     *
     * @return integer
     */
    public function getCodigo()
    {
        return $this->codigo;
    }
}
