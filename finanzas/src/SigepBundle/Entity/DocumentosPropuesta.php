<?php

namespace SigepBundle\Entity;

/**
 * DocumentosPropuesta
 */
class DocumentosPropuesta
{
    /**
     * @var string
     */
    private $nombre;

    /**
     * @var string
     */
    private $documento;

    /**
     * @var integer
     */
    private $codigo;

    /**
     * @var \SigepBundle\Entity\Propuestas
     */
    private $propuesta;


    /**
     * Set nombre
     *
     * @param string $nombre
     *
     * @return DocumentosPropuesta
     */
    public function setNombre($nombre)
    {
        $this->nombre = $nombre;

        return $this;
    }

    /**
     * Get nombre
     *
     * @return string
     */
    public function getNombre()
    {
        return $this->nombre;
    }

    /**
     * Set documento
     *
     * @param string $documento
     *
     * @return DocumentosPropuesta
     */
    public function setDocumento($documento)
    {
        $this->documento = $documento;

        return $this;
    }

    /**
     * Get documento
     *
     * @return string
     */
    public function getDocumento()
    {
        return $this->documento;
    }

    /**
     * Get codigo
     *
     * @return integer
     */
    public function getCodigo()
    {
        return $this->codigo;
    }

    /**
     * Set propuesta
     *
     * @param \SigepBundle\Entity\Propuestas $propuesta
     *
     * @return DocumentosPropuesta
     */
    public function setPropuesta(\SigepBundle\Entity\Propuestas $propuesta = null)
    {
        $this->propuesta = $propuesta;

        return $this;
    }

    /**
     * Get propuesta
     *
     * @return \SigepBundle\Entity\Propuestas
     */
    public function getPropuesta()
    {
        return $this->propuesta;
    }
}
